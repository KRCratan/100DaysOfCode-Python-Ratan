# Arithmetic Operators
a = 10
b = 3

print(a + b)
print(a / b)
print(a // b)
print(a ** b)

# Comparison Operators
x = 10
y = 20

print(x == y)
print(x < y)
print(x >= 5)

#Logical Operators
age = 18
is_student = True

print(age >= 18 and is_student)
print(age < 18 or is_student)
print(not is_student)

# Assignment Operators
r = 10
r += 5
r *= 2

print(r)
